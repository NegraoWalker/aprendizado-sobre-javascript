// Exercício 2: Você comprou um produto por R$ 100,00 e deseja vender com um lucro adicional
// de R$ 30,00. Calcule o preço de venda do produto.

const VALOR_DE_COMPRA_DO_PRODUTO = 100.00;
const LUCRO = 30.00;

let precoDeVendaDoProduto = VALOR_DE_COMPRA_DO_PRODUTO + LUCRO;
console.log(`Preço de venda do produto: R$${precoDeVendaDoProduto}`);